# PATCH LR: Warmup + Cosine (dtype fix) + Recompile
# ============================
import math, tensorflow as tf

# 1) steps/epoch dal conteggio noto (robusto)
steps_per_epoch = max(1, math.ceil(TRAIN_SAMPLES / BATCH_SIZE))
